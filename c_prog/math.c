#include<stdio.h>
#include<math.h>
void main(){
    printf("ceil(12.6)=%f\n", ceil(12.6));
    printf("floor(12.6)=%f\n", floor(12.6));
    printf("sqrt(125)=%f\n", sqrt(125));
    printf("pow(4,3)=%f\n", pow(4,3));
    printf("abs(-12.6)=%f\n", abs(-12.6));
    printf("log(12.6)=%f\n", log(12.6));
    printf("sin(12.6)=%f\n", sin(12.6));
    printf("cos(12.6)=%f\n", cos(12.6));
}
